#include "push_swap.h"

int	is_integer(char *s)
{
	int i;

	i = 0;
	if(s[i] == '-' || s[i] == '+')
		i++;
	while(s[i] != 0)
	{
		if (!ft_isdigit(s[i]))
			return(0);
		else
			i++;
	}
	return (1);
}

